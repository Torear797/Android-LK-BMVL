package com.bmvl.lk.Rest;

public class SuggestionMethod {
    private String method_name;
    private String value;
    private Short id;
    private String name_document;
    private Short id_document;

    public String getMethod_name() {
        return method_name;
    }

    public void setMethod_name(String method_name) {
        this.method_name = method_name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public Short getId() {
        return id;
    }

    public void setId(Short id) {
        this.id = id;
    }

    public String getName_document() {
        return name_document;
    }

    public void setName_document(String name_document) {
        this.name_document = name_document;
    }

    public Short getId_document() {
        return id_document;
    }

    public void setId_document(Short id_document) {
        this.id_document = id_document;
    }
}
